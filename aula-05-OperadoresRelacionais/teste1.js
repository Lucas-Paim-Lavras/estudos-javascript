/*
    == igual (com coerção de tipo)

    === estritamente igual (sem coerção)

    != diferente

    !== estritamente diferente

    > maior que

    < menor que

    >= maior ou igual

    <= menor ou igual
*/

let num1 = 8
let num2 = 4
let num3 = 8

console.log (num1 > num3)
console.log (num1 >= 3)
console.log (!(num1 == num3)) //o ! (not) inverte a resposta do operador relacional. Nesse caso aqui, teríamos que num1 é igual ao num3 mas o ! antes faz com que retorne False
