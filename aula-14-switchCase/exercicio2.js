let dia = Number(prompt("Digite um número de 1 a 7: "))
switch (dia){
    case 1:
        console.log("Resultado: " + "Domingo")
        break
    case 2:
        console.log("Resultado: " + "Segunda-feira")
        break
    case 3:
        console.log("Resultado: " + "Terça-feira")
        break
    case 4:
        console.log("Resultado: " + "Quarta-feira")
        break
    case 5:
        console.log("Resultado: " + "Quinta-feira")
        break
    case 6:
        console.log("Resultado: " + "Sexta-feira")
        break
    case 7:
        console.log("Resultado: " + "Sábado")
        break
    default:
        console.log("Dia inválido")

}